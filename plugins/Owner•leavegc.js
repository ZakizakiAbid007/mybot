const handler = async (m, { conn, text, command }) => {
    // تحديد ID المجموعة. إذا لم يتم تحديد مجموعة، يستخدم المجموعة الحالية.
    let id = text ? text : m.chat; 
    let chat = global.db.data.chats[m.chat]; // جلب بيانات الدردشة من DB (لتعطيل رسالة الترحيب مؤقتاً)
    
    // 1. محاولة تعطيل رسالة الترحيب مؤقتاً
    // هذا يمنع البوت من إرسال "رسالة المغادرة" عند مغادرته (إذا كانت هذه الميزة مفعلة)
    if (chat) { // تأكد من أن الدردشة مسجلة في قاعدة البيانات
        chat.welcome = false;
    }
    
    // 2. إرسال رسالة المغادرة
    await conn.reply(id, `《✧》 *Yotsuha* يغادر المجموعة، كان من الرائع التواجد هنا.`, m); // إضافة m لضمان الرد على الرسالة
    
    // 3. تنفيذ أمر المغادرة
    await conn.groupLeave(id);
    
    // 4. إعادة تفعيل رسالة الترحيب (للمجموعة الحالية فقط، إذا كان الأمر يتضمنها)
    try {
        if (chat) { // إعادة تفعيل رسالة الترحيب للمجموعة التي تم تنفيذ الأمر فيها
            chat.welcome = true;
        }
    } catch (e) {
        // في حال حدوث خطأ عند محاولة إعادة التفعيل
        // (ملاحظة: المتغير fg غير معرَّف في الكود الأصلي، لذا تم استبداله برسالة خطأ)
        await m.reply(`🚩 حدث خطأ عند محاولة إعادة تفعيل رسالة الترحيب. يرجى التحقق يدوياً.`); 
        return console.log(e);
    }
};

// معلومات تعريف الأمر
handler.command = ['مغادرة', 'اخرج', 'leave', 'leavegc', 'salir']; // الأوامر التي يتم بها تفعيل الدالة
handler.group = true; // يعمل في المجموعات
handler.rowner = true; // يتطلب أن يكون المستخدم مالكاً رئيسياً للبوت
export default handler;