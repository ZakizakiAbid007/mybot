// الكود للملك تنغن 👑 - نظام الإبلاغ عن الأخطاء
const handler = async (m, { conn, text, usedPrefix, command }) => {
    
    // شروط إدخال النص (صارمة)
    // تم تحديث رسالة الخطأ لتضمين مثال واضح لاستخدام الأمر (مثال: .ابلاغ)
    if (!text) throw `⚠ *_أدخل تفاصيل الخطأ/العطل الذي تود الإبلاغ عنه بوضوح. مثال الاستخدام: ${usedPrefix}ابلاغ [نص التقرير]._*`
    if (text.length < 10) throw '⚠️ *_الرجاء تحديد الخطأ بدقة، الحد الأدنى 10 أحرف._*'
    if (text.length > 1000) throw '⚠️ *_الحد الأقصى لرسالة التقرير هو 1000 حرف. كن مختصراً._*'
    
    // تجهيز رسالة التقرير
    const teks = `╭───────────────────
│⊷〘 *تَـقْـرِيـرٌ مَـلَـكِـي* 📜 〙⊷
├───────────────────
│⁖👤 *الـمُـبْـلِـغ:*
│✏️ Wa.me/${m.sender.split`@`[0]}
│
│⁖📩 *نَـصُّ الـتَّـقْـرِيـرِ:*
│📝 ${text}
╰───────────────────`
    
    // إرسال التقرير إلى المالك الأول (global.owner[0])
    try {
        // ملاحظة: الكود لا يزال يرسل الرسالة المقتبسة تلقائياً إذا قام المستخدم بالرد
        await conn.reply(global.owner[0][0] + '@s.whatsapp.net', m.quoted ? teks + '\n\n*الرسالة المُقتبسة:*\n' + m.quoted.text : teks, m, { mentions: conn.parseMention(teks) })
        
        // رسالة التأكيد للمستخدم
        m.reply('✅ *تم إرسال التقرير بنجاح إلى القائد.* أي تقرير كاذب أو تافه قد يعرضك للحظر القاسي! ⛓️')
    } catch (e) {
        console.error(e)
        m.reply('❌ *فشل إرسال التقرير.* قد يكون المالك غير متوفر حالياً. 🤷‍♂️')
    }
}

handler.help = ['إبلاغ']
handler.tags = ['أساسي']
handler.command = ['reporte','report','reportar','bug','error', 'ابلاغ', 'تقرير']

export default handler