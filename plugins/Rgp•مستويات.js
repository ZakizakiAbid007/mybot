let handler = m => m
handler.before = async function (m) {
    // 1. استخراج بيانات المستخدم
    if (!global.db.data.users) return // للتأكد من وجود قاعدة البيانات
    let user = global.db.data.users[m.sender]
    if (!user) return // تجاهل إذا لم يكن المستخدم موجوداً

    let level = user.level || 0

    // 2. تحديد الرتبة المناسبة
    // يتم فرز الرتب تنازلياً بناءً على المستوى الأدنى
    // ثم يتم البحث عن أول رتبة يتجاوز أو يساوي مستواها مستوى المستخدم
    let role = (Object.entries(roles).sort((a, b) => b[1] - a[1]).find(([, minLevel]) => level >= minLevel) || Object.entries(roles)[0])[0]
    
    // 3. تحديث الرتبة في قاعدة البيانات
    user.role = role
    
    return !0 // استمرار مع معالجات الرسائل الأخرى
}
export default handler